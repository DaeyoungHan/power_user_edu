a = 5
b = 2

# 산술 연산자
print(a + b)    # 더하기
print(a - b)    # 빼기
print(a * b)    # 곱하기
print(a / b)    # 나누기
print(a % b)    # 나머지
print(a // b)   # 몫
print(a**b)     # 거듭 제곱


# 비교 연산자
print(a > b)    # 크다
print(a >= b)   # 크거나 같다
print(a < b)    # 작다
print(a <= b)   # 작거나 같다
print(a == b)   # 같다
print(a != b)   # 같지 않다

# 논리 연산자
print(a < b and b != 0) # 둘다 참이면 True
print(a < b or b != 1)  # 하나라도 참이면 True
print(not a < b)        # 반전

# 멤버 연산자
print('c' in 'cat')     # 포함이면 True
print('c' not in 'cat') # 미포함이면 True